
let num=1

switch(num){
    case 1: console.log("Number is one")
    break

    case 2: console.log("Number is two")
    break;

    case 3: console.log("Number is three")
    break;

    case 4: console.log("Number is four")
    break;

    case 5: console.log("Number is five")
    break;

    default: console.log("Not a valid input")
    break;
}

